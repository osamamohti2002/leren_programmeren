aantal_lijstjes = int(input("Voer het aantal lijstjes in: "))


lijst = []
for i in range(aantal_lijstjes):
    langte = int(input('wat is de langte van je lijst'))
    lijst.append(langte)
    print(lijst)

for i in lijst:
    print(i)



